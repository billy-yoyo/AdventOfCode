def single_round():
    swaps = 0
    i = 0
    while i < len(input_list) - 1:
        if input_list[i] < input_list[i + 1]:
            input_list[i], input_list[i + 1] = input_list[i + 1], input_list[i]
            swaps += 1
        i += 1
    return swaps

